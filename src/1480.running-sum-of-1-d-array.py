# [1480] Running Sum of 1d Array
#

# @lc code=start
class Solution:
    # 36 ms, 91.05%. Time: O(N), Space: O(1)
    def runningSum(self, nums: List[int]) -> List[int]:
        for i in range(1, len(nums)):
            nums[i] += nums[i - 1]
        return nums
